<b><p align="left">Fasilitas Green Canyon</p></b>
<br>
<p align="left">Untuk memenuhi kenyamanan para pengunjung saat berwisata  pihak pengelola menyediakan beberapa fasilitas penunjang serta kebutuhan para pengunjung dalam area wisata antara lain penginapan, food court, toilet, mushola, dan penyewaan body rafting.</p>
<br>
<p align="left">Pesan bagi yang ingin berkunjung, tetaplah jaga kebersihan dan perilakumu dimanapun kamu berada ya, karena menjaga kebersihan dan kelestarian lingkungan wisata merupakan kewajiban kita agar kelak dapat dinikmati pula oleh anak serta cucu kita.  have fun guys!</p>
