</div>
<br>
<center><img src="Images/cukang1.jpg" alt="cukang" width="500" height="300"></center>
<br>
<p align="left">Siapa tak kenal Green Canyon, Objek wisata dengan nama Green Canyon dipopulerkan oleh seorang Perancis pada tahun 1993 Namun, orang Sunda menyebut Green Canyon dengan sebutan Cukang Taneuh atau dalam bahasa Indonesia berarti Jembatan Tanah. Nama Green Canyon ini juga merupakan pelesetan dari nama Grand Canyon yang ada di Colorado, Amerika Serikat. Terletak di Desa Kertayasa Kecamatan Cijulang, Kabupaten Pangandaran 31 km dari Pangandaran.</p>
<br>
<center><img src="Images/cukang2.jpg" alt="cukang" width="500" height="300"></center>
<br>
<p align="left">Berbentuk ngarai yang terjadi dari erosi tanah akibat aliran sungai Cijulang selama jutaan tahun yang menembus gua dengan stalaktit dan stalakmit yang mempesona serta diapit oleh dua bukit dengan bebatuan dan rimbunnya pepohonan menyajikan atraksi alam yang khas dan menantang. Untuk mencapai tempat ini, kita harus menyewa sebuah perahu kayuh dari dermaga Ciseureuh. Perjalanannya memakan waktu kurang lebih 30-45 menit dengan jarak sekitar 3 km untuk sampai ke Green Canyon. Harga sampannya Rp.150.000 untuk maksimal 5 orang. Sepanjang perjalanan, mata kita akan dimanjakan oleh hijau teduhnya warna air sungai. Di mulut gua terdapat air terjun palatar sehingga suasana di objek wisata ini terasa begitu sejuk.</p>
<br>
<center><img src="Images/cukang3.jpg" alt="cukang" width="500" height="300"></center>
<br>
<p align="left">Kegiatan yang dapat dilakukan diantaranya panjat tebing, berenang, bermain kayak, bersampan sambil memancing. Untuk mencapai lokasi ini wisatawan dapat menggunakan perahu yang banyak tersedia di Dermaga Ciseureuh baik perahu tempel maupun perahu kayuh. Objek wisata ini berdekatan degan objek wisata Batu Karas serta Bandar Udara Nusawiru.</p>
<br>
<p align="left">Tempat wisata ini beroperasi setiap hari dari pukul 08.00 â€“ 16.00 WIB. </p>
