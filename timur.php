<br>
<p align="left">Objek wisata yang merupakan primadona pantai di Jawa Barat ini terletak di Desa Pananjung Kecamatan Pangandaran dengan jarak Â± 92 km arah selatan kota Ciamis, pantai yang memiliki pasir putih yang elok ini ternyata menyimpan berbagai hal yang menarik yang mana banyak orang yang tidak mengetahuinya.</p>
<br>
<p align="left">Tidak jauh dari taman wisata cagar alam Pangandaran, yaitu tepatnya di pantai timur Pangandaran itu sendiri terdapat suatu tempat yang wajib dikunjungi oleh meraka yang sangat menyukai keindahan bawah laut.</p>
<br>
<center><img src="Images/timur4.jpg" alt="timur"style="width:40%;"></center>
<br>
<p align="left">Terkenal akan keindahan alam bawah laut serta keberagaman biota lautnya, pada bagian timur pantai Pangandaran sering dijadikan spot untuk melakukan snorkeling. Untuk menikmati keindahan bawah laut ini, maka setiap orang akan dikenakan biaya tambahan.</p>
<br>
<center><img src="Images/timur2.jpg" alt="timur"style="width:40%;"></center>
<br>
<p align="left">Tidak hanya itu saja, pantai timur Pangandaran ini juga sangat terkenal dengan keindahan pantainya di saat matahari akan mulai terbit. Tidak jarang jika banyak wisatawan yang akan menghabiskan waktu di sisi timur pantai Pangandaran pada pagi hari. Keindahan yang tidak ada duanya ini dijadikan sebagai tempat bagi para wisatawan untuk berswafoto.</p>
<br>
<p align="left">Keindahan yang ada pada sisi timur ini meliputi keindahan taman wisata alam yang terdapat di sisi kiri dan pulau Nusakambangan yang terletak di sisi kanan. Bahkan, jika anda mengunjungi saat cuaca sedang cerah, maka anda akan bisa melihat keagungan Gunung Slamet yang sangat mempesona.</p>
<br>
