<b><p align="left">4. Soto Jarkomi</p></b>
<br>
<center><img src="images/kuliner4.jpg" alt="pindang" width="500" height="300"></center>
<br>
<p align="left">Meskipun di berbagai daerah di Indonesia ada banyak sekali jenis soto dengan ciri khas masing-masing namun jangan salah, pangandaran juga punya soto andalannya bernama soto jarkomi yang tak kalah lezat dengan soto lainnya. Kuliner satu ini akan memanjakan lidah wisatawan dengan racikan bumbu-bumbu rempah alami bersama irisan daging ayam kampung, bihun, kacang tanah, bawang goreng, seledri serta di lengkapi kerupuk merah khas soto jarkomi sehingga semua aroma kesegarannya tercium ketika mangkuk soto tersebut mendarat di meja.</p>
