<b><p align="left">1. Pindang Gunung</p></b>
<br>
<center><img src="images/kuliner1.jpg" alt="entog" width="500" height="300"></center>
<br>
<p align="left"> Jalan-jalan ke Pantai Pangandaran, Jawa Barat, tak lengkap rasanya bila tak menikmati sajian ikan laut segar di sepanjang jalan pantai timur. Salah satunya adalah pindang gunung. Sebuah sajian khas laut pangandaran yaitu sup ikan berkuah dengan bumbu khas sunda berupa honje atau kecombrang, daun kedondong, daun ruku-ruku dan lengkuas berpadu menciptakan sensi asem segar serta aroma yang menggoda di lidah.</p>
<br>
<b><p align="left"> Kuliner ini dapat di temui di tempat tempat sebagai berikut :</p></b>
