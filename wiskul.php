<b><p align="left">3. Sate Galunggung</p></b>
<br>
<center><img src="images/kuliner3.jpg" alt="burbacek" width="500" height="300"></center>
<br>
<p align="left">Pangandaran tak selalu tentang seafood karena terdapat juga kuliner berbahan dasar daging yang rasanya lezat, namanya Sate Galunggung. Seporsi olahan sate ini kita akan diberi sepuluh tusuk sate dengan bumbu kacang yang diberi kecap. Tidak lupa juga sambal kecap khas sate kambing, nasi, kerupuk emping, dan acar timun. Rasa daging ayam dan kambing yang sedikit menghitam makin gurih saat dicocol di sambal kacang.  Kelezatan sate ini cocok dipadukan dengan nasi yang diberi kuah sop buntut atau tengkleng yang gurih. Yummmy !</p>
<br>
<b><p align="left"> Kuliner ini dapat di temui di tempat tempat sebagai berikut :</p></b>
<br>
<p align="left">Jl. Kidang Pananjung, Pananjung, Kec. Pangandaran, Pangandaran, Jawa Barat, buka setiap hari dari jam 10.00-15.00</p>
<p align="left"><a href="https://www.google.com/maps/dir/-6.1810959,106.994906/sate+galunggung/@-6.9324768,106.8157827,8z/data=!3m1!4b1!4m9!4m8!1m1!4e1!1m5!1m1!1s0x2e6598675ed65747:0xcba51b28bb06f53d!2m2!1d108.6579837!2d-7.6910984">GMaps Sate Galunggung</a></p>
