<center><h1><span style="color: black; font-family: Verdana,Arial,Helvetica,Georgia; font-size: 25px;">PANTAI BATU KARAS</span></h1></center>
</div>
<br>
<center><img src="Images/batu1.jpg" alt="batu" width="500" height="300"></center>
<BR>
<p align="left">Objek wisata yang satu ini merupakan perpaduan nuansa alam antara objek wisata Pangandaran dan Batu Hiu dengan suasana alam yang tenang, gelombang laut yang bersahabat dengan pantainya yang landai membuat pengunjung kerasan tinggal di kawasan ini. Terletak di Desa Batukaras, Kecamatan Cijulang dengan jarak ± 34 km dari Pangandaran.</p>
<br>
<p align="left">Pantainya yang landai dengan air laut tenang nan biru menanti Anda untuk segera berenang menikmati airnya yang segar. Anda bisa nikmati suasana tenang dengan angin sepoi-sepoi menikmati hidangan di rumah makan yang tersedia. Pandangan lepas ke ujung cakrawala memberi Anda ketenangan dan kenangan berlibur yang menyenangkan.</p>
<br>
<center><img src="Images/batu2.jpg" alt="batu" width="500" height="300"></center>
<br>
<p align="left">Kegiatan wisata yang dapat dilakukan selain berenang antara lain: berperahu di bengawan, berkemah dan berselancar. Jika liburan Anda bersama keluarga, akomodasi telah tersedia untuk Anda, ada pondok wisata yang dilengkapi dengan arena bermain dan rumah ibadah. Pondok wisata ini dikelola langsung oleh Diparda Kabupaten Ciamis. Fasilitas lainnya yang tersedia antara lain: Hotel, Camping Ground, Kios Cinderamata, sewaan papan selancar dan ban renang.</p>
<br>
<p align="left">Pengelola Pantai Batu Karas juga memperbolehkan anda menggunakan kamera DSLR. Maka, jika anda termasuk penggemar fotografi anda bisa dengan puas mengabadikan foto di tempat ini. Temapt ini juga dapat dijadikan tempat Pre-Wedding, namun tentunya dengan seizin dari pihak pengelola.</p>
<br>
<p align="left">Tempat wisata ini beroperasi setiap hari dari pukul 07.30 – 18.00 WIB. untuk harga tiket masuk pihak pengelola mematok harga Rp. 15.000 hingga Rp. 65.000 untuk setiap kendaraan.</p>
<br>
<center><img src="Images/batu3.jpg" alt="karangsong" width="500" height="300"></center>
<br>
<b><p align="left">Fasilitas Pantai Batu Karas</p></b>
<br>
<p align="left">Untuk memenuhi kenyamanan para pengunjung saat berwisata  pihak pengelola menyediakan beberapa fasilitas penunjang serta kebutuhan para pengunjung dalam area wisata antara lain penginapan, food court, toilet, mushola, penyewaan surfing.</p>
<br>
<p align="left">Pesan bagi yang ingin berkunjung, tetaplah jaga kebersihan dan perilakumu dimanapun kamu berada ya, karena menjaga kebersihan dan kelestarian lingkungan wisata merupakan kewajiban kita agar kelak dapat dinikmati pula oleh anak serta cucu kita.  have fun guys!</p>
<br>
