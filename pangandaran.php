<center><img src="images/pangandaran1.jpg" alt="pangandaran" width="500" height="300"></center>
<br>
<p align="left">Wisata alam di Jawa Barat gak melulu soal curug (air terjun) saja. Ada begitu banyak tempat indah yang tersebar di sana, khususnya daerah Pangandaran. Terletak di paling selatan Jawa Barat, kamu bisa menemukan pantai eksotis bernama Pangandaran.</p>
<br>
<p align="left">Pantai Pangandaran adalah salah satu pantai yang berada di Kabupaten Pangandaran yang paling populer untuk dijadikan tempat wisata saat akhir pekan maupun liburan. Pantai ini memiliki panorama yang sangat indah lengkap dengan flora dan faunanya. Pantai Pangandaran memiliki 2 sisi, yang pertama adalah pasir coklat kehitaman dan yang kedua adalah pasir putih. Saat pertama masuk sepanjang jalan anda akan melihat pantai dengan pasir coklat, di pantai ini cocok untuk berenang dan bermain surfing. Sedangkan jika anda lebih suka snorkeling, pasir putih sangat cocok karena banyak sekali biota bawah laut lengkap dengan terumbu karang yang siap memanjakan mata anda.</p>
<br>
<center><img src="images/pangandaran3.jpg" alt="pangandaran" width="500" height="300"></center>
<br>
<p align="left">Adapun acara tradisional yang terdapat di sini adalah Hajat Laut, yakni upacara yang dilakukan nelayan di Pangandaran sebagai perwujudan rasa terima kasih mereka terhadap kemurahan Tuhan YME dengan cara melarung sesajen ke laut lepas. Acara ini biasa dilaksanakan pada tiap-tiap bulan Muharam.</p>
<br>
<center><img src="images/timur4.jpg" alt="pangandaran" width="500" height="300"></center>
<br>
<p align="left">Berenang atau berjemur jadi sudah hal biasa yang dilakukan ketika bermain di Pantai. Di Pantai Pangandaran, kamu bisa mendapatkan pengalaman yang lebih seru dan menarik sebab Pantai Pangandaran menyediakan beragam pilihan watersport, seperti banana boat, jetski, big mable boat, aqua glade, dan sebagainya. Harganya dibanderol mulai Rp30-300 ribu.</p>
<br>
<p align="left">Tempat wisata ini beroperasi setiap hari dari pukul 07.30 â€“ 18.00 WIB. untuk harga tiket masuk pihak pengelola mematok harga bervariasi mulai Rp. 6.000 bagi pejalan kaki dan bagi kendaraan mulai dari Rp. 15.000 hingga Rp. 93.000. </p>
